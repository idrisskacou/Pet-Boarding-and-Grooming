
public class Dog {
	//Define Variable 
	String petType;
	String petName;
	int petAge;
	String dogSpaces;
	int daysStay;
	int amountDue;
	
	/**
	 * @return the petType
	 */
	public String getPetType() {
		return petType;
	}
	/**
	 * @param petType the petType to set
	 */
	public void setPetType(String petType) {
		this.petType = petType;
	}
	/**
	 * @return the petName
	 */
	public String getPetName() {
		return petName;
	}
	/**
	 * @param petName the petName to set
	 */
	public void setPetName(String petName) {
		this.petName = petName;
	}
	/**
	 * @return the petAge
	 */
	public int getPetAge() {
		return petAge;
	}
	/**
	 * @param petAge the petAge to set
	 */
	public void setPetAge(int petAge) {
		this.petAge = petAge;
	}
	/**
	 * @return the dogSpaces
	 */
	public String getDogSpaces() {
		return dogSpaces;
	}
	/**
	 * @param dogSpaces the dogSpaces to set
	 */
	public void setDogSpaces(String dogSpaces) {
		this.dogSpaces = dogSpaces;
	}
	/**
	 * @return the daysStay
	 */
	public int getDaysStay() {
		return daysStay;
	}
	/**
	 * @param daysStay the daysStay to set
	 */
	public void setDaysStay(int daysStay) {
		this.daysStay = daysStay;
	}
	/**
	 * @return the amountDue
	 */
	public int getAmountDue() {
		return amountDue;
	}
	/**
	 * @param amountDue the amountDue to set
	 */
	public void setAmountDue(int amountDue) {
		this.amountDue = amountDue;
	}

}