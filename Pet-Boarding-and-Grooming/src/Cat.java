
public class Cat {
	//Define Variable 
	String petName;
	int petAge;
	String catSpaces;
	int daysStay;
	int amountDue;
	private String breed;
	
	String petType;
	/**
	 * @return the petType
	 */
	public String getPetType() {
		return petType;
	}
	/**
	 * @param petType the petType to set
	 */
	public void setPetType(String petType) {
		this.petType = petType;
	}
	/**
	 * @return the petName
	 */
	public String getPetName() {
		return petName;
	}
	/**
	 * @param petName the petName to set
	 */
	public void setPetName(String petName) {
		this.petName = petName;
	}
	/**
	 * @return the petAge
	 */
	public int getPetAge() {
		return petAge;
	}
	/**
	 * @param petAge the petAge to set
	 */
	public void setPetAge(int petAge) {
		this.petAge = petAge;
	}
	/**
	 * @return the catSpaces
	 */
	public String getCatSpaces() {
		return catSpaces;
	}
	/**
	 * @param catSpaces the catSpaces to set
	 */
	public void setCatSpaces(String catSpaces) {
		this.catSpaces = catSpaces;
	}
	/**
	 * @return the daysStay
	 */
	public int getDaysStay() {
		return daysStay;
	}
	/**
	 * @param daysStay the daysStay to set
	 */
	public void setDaysStay(int daysStay) {
		this.daysStay = daysStay;
	}
	/**
	 * @return the amountDue
	 */
	public int getAmountDue() {
		return amountDue;
	}
	/**
	 * @param amountDue the amountDue to set
	 */
	public void setAmountDue(int amountDue) {
		this.amountDue = amountDue;
	}
	/**
	 * @return the breed
	 */
	public String getBreed() {
		return breed;
	}
	/**
	 * @param breed the breed to set
	 */
	public void setBreed(String breed) {
		this.breed = breed;
	}

}
